    private void bottigliaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bottigliaActionPerformed
        if (drunk == false) {
            goScene(this);
            drunk = true;
        } else {
            this.text.setText("la bottiglia è vuota");
            Timer timer = new Timer(3000, event -> {
                text.setText("");
            });
            timer.setRepeats(false);
            timer.start();
        }
    }//GEN-LAST:event_bottigliaActionPerformed

