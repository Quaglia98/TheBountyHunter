        @Override
        public void windowOpened(WindowEvent arg0) {

            SwingWorker<Void, String> worker = new SwingWorker<Void, String>() {

                @Override
                protected Void doInBackground() throws Exception {
                    this.publish(new String[]{"C'è ancora un pò di rum in questa bottiglia"});
                    Thread.sleep(5000);
                    this.publish(new String[]{"Vuoi finirla prima di tornare a casa?"});
                    Thread.sleep(5000);
                    this.publish("");
                    EntrataSaloon.this.avanzamento.setVisible(true);
                    EntrataSaloon.this.bottiglia.setVisible(true);
                    return null;
                }

                @Override
                protected void process(List<String> res) {
                    res.forEach(thistext -> {
                        text.setText(thistext);
                    });
                }

            };

            worker.execute();

        }

